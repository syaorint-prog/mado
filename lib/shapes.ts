const Y_OFFSET = 25;

export const getShapeCoords = (shape: ShapeType | null): [number, number, number][] => {
  if (shape === 'robot') return ROBOT_COORDS.map(([x, y, z]) => [x, y + Y_OFFSET, z]);
  if (shape === 'car') return CAR_COORDS.map(([x, y, z]) => [x, y + Y_OFFSET, z]);
  return [];
};
